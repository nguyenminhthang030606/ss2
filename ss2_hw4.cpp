#include <stdio.h>

int main() {
    // Khoi tao bien canh vuong va gan gia tri 
    int side = 5; // Gia tri canh vuong là 5

    // Tinh chu vi và dien tich
    int perimeter = 4 * side;          // Chu vi: 4 lan canh
    int area = side * side;            // Dien tich: canh binh phuong

    

   
// In ket qua ra màn hinh
    printf("Chu vi hinh vuong: %d\n", perimeter);
    printf("Dien tich hinh vuong: %d\n", area);

    return 0;
}
